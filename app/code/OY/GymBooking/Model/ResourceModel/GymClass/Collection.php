namespace OY\GymBooking\Model\ResourceModel\GymClass;

class Collection extends \Magento\Framework\Model\ResourceModel\Db\Collection\AbstractCollection
{
    protected function _construct()
    {
        $this->_init('OY\GymBooking\Model\GymClass','OY\GymBooking\Model\ResourceModel\GymClass');
    }

    protected function _beforeLoad()
    {
        parent::_beforeLoad(); // TODO: Change the autogenerated stub

        return $this;
    }

    public function toOptionArray()
    {
        $options = [];
        $propertyMap = [
            'value' => 'value_id',
        ];

        foreach ($this as $item) {
            $option = [];
            foreach ($propertyMap as $code => $field) {
                $option[$code] = $item->getData($field);
            }
            $option['label'] = $item->getName();
            $options[] = $option;
        }

        if (count($options) > 0) {
            array_unshift(
                $options,
                ['value' => null, 'label' => ' ']
            );
        }

        return $options;
    }

}
